/*
 * LeetCode 523: 连续的子数组和 (Continuous Subarray Sum)
 * 难度：Medium
 *
 * ============================================================================
 * 题目概述
 * ============================================================================
 * 给你一个整数数组 nums 和一个整数 k，如果 nums 有一个长度至少为 2 的连续子数组，
 * 其元素总和为 k 的倍数，则返回 true；否则，返回 false。
 *
 * 如果存在一个整数 n，令 sum = n * k，则称 sum 为 k 的倍数。
 * 0 始终视为 k 的一个倍数。
 *
 * 关键约束：
 * - 1 <= nums.length <= 10^5
 * - 0 <= nums[i] <= 10^9
 * - 0 <= sum(nums[i]) <= 2^31 - 1
 * - 1 <= k <= 2^31 - 1
 * - 子数组长度至少为 2
 *
 * ============================================================================
 * 示例
 * ============================================================================
 * 示例 1:
 * 输入：nums = [23,2,4,6,7], k = 6
 * 输出：true
 * 解释：[2, 4] 是一个大小为 2 的子数组，并且和为 6
 *
 * 示例 2:
 * 输入：nums = [23,2,6,4,7], k = 6
 * 输出：true
 * 解释：[23, 2, 6, 4, 7] 是大小为 5 的子数组，并且和为 42 (42 = 7 * 6)
 *
 * 示例 3:
 * 输入：nums = [23,2,4,6,7], k = 13
 * 输出：false
 *
 * ============================================================================
 * 核心思想：前缀和 + 哈希表 + 同余定理 + 长度限制
 * ============================================================================
 *
 * 1. 同余定理应用：
 *    如果 (prefixSum[j] - prefixSum[i]) % k == 0
 *    则 prefixSum[j] % k == prefixSum[i] % k
 *
 * 2. 长度限制处理：
 *    子数组长度至少为 2，即 j - i >= 2
 *    在哈希表中记录每个余数第一次出现的索引
 *    当再次遇到相同余数时，检查索引差是否 >= 2
 *
 * 3. 特殊情况：
 *    - k = 0：题目保证 k >= 1，不需要处理
 *    - 余数为 0：说明前缀和本身就是 k 的倍数
 *
 * 4. 算法流程：
 *    - 使用哈希表记录每个余数第一次出现的索引
 *    - 遍历数组，计算当前前缀和的余数
 *    - 如果余数之前出现过，检查长度是否 >= 2
 *    - 如果满足条件，返回 true
 *
 * ============================================================================
 * 与 LeetCode 974 的区别
 * ============================================================================
 *
 * LeetCode 974: 和可被 K 整除的子数组
 * - 目标：统计所有满足条件的子数组个数
 * - 哈希表存储：每个余数出现的次数
 * - 无长度限制
 *
 * LeetCode 523: 连续的子数组和
 * - 目标：判断是否存在满足条件的子数组
 * - 哈希表存储：每个余数第一次出现的索引
 * - 长度至少为 2
 *
 * ============================================================================
 * 前缀和原理图解
 * ============================================================================
 *
 * 数组：     [23,  2,  4,  6,  7]    k = 6
 * 索引：      0    1   2   3   4
 *
 * 前缀和和余数：
 * i=-1: prefixSum=0,  余数=0, 索引=-1  (初始化)
 * i=0:  prefixSum=23, 余数=5, 索引=0
 * i=1:  prefixSum=25, 余数=1, 索引=1
 * i=2:  prefixSum=29, 余数=5, 索引=2  <- 余数5之前在索引0出现过
 *                                        长度 = 2 - 0 = 2 >= 2 ✓
 *                                        子数组 [2, 4]，和 = 6
 *
 * 结果：找到满足条件的子数组，返回 true
 *
 * ============================================================================
 * 长度限制的处理
 * ============================================================================
 *
 * 为什么要记录索引而不是计数：
 * - 需要检查子数组长度是否 >= 2
 * - 记录第一次出现的索引，可以计算最大长度
 * - 如果记录计数，无法判断长度
 *
 * 示例：nums = [0, 0], k = 1
 * i=0: prefixSum=0, 余数=0, 已存在索引-1, 长度=0-(-1)=1 < 2 ✗
 * i=1: prefixSum=0, 余数=0, 已存在索引-1, 长度=1-(-1)=2 >= 2 ✓
 *
 * ============================================================================
 * 复杂度分析
 * ============================================================================
 *
 * 方法1：前缀和 + 哈希表 + 同余（最优）
 * 时间复杂度：O(n) - 遍历一次数组
 * 空间复杂度：O(min(n, k)) - 哈希表最多存储 k 个不同的余数
 *
 * 方法2：暴力枚举
 * 时间复杂度：O(n²) - 双重循环
 * 空间复杂度：O(1)
 *
 * ============================================================================
 * 特殊情况处理
 * ============================================================================
 *
 * 1. 长度限制：子数组长度至少为 2
 * 2. 初始化：remainderIndex[0] = -1，处理从索引 0 开始的子数组
 * 3. 索引记录：只记录每个余数第一次出现的索引（保证最大长度）
 * 4. 边界情况：数组长度为 1 时，直接返回 false
 *
 */

#include <iostream>
#include <vector>
#include <unordered_map>
using namespace std;

class Solution {
public:
    // ========================================================================
    // 方法1：前缀和 + 哈希表 + 同余定理（最优解法）
    // ========================================================================
    // 核心思想：
    // 1. 使用哈希表记录每个余数第一次出现的索引
    // 2. 如果当前余数之前出现过，检查长度是否 >= 2
    // 3. 如果满足条件，返回 true
    // ========================================================================
    bool checkSubarraySum(vector<int>& nums, int k) {
        int n = nums.size();
        if (n < 2) return false;  // 长度至少为 2

        // 哈希表：key = 余数，value = 该余数第一次出现的索引
        unordered_map<int, int> remainderIndex;

        // 初始化：余数为 0 在索引 -1 出现
        // 这是为了处理从索引 0 开始的子数组
        remainderIndex[0] = -1;

        int prefixSum = 0;  // 当前前缀和

        // 遍历数组，计算前缀和
        for (int i = 0; i < n; i++) {
            // 更新前缀和
            prefixSum += nums[i];

            // 计算余数
            int remainder = prefixSum % k;

            // 检查这个余数是否之前出现过
            if (remainderIndex.find(remainder) != remainderIndex.end()) {
                // 计算子数组长度
                int length = i - remainderIndex[remainder];

                // 如果长度 >= 2，找到满足条件的子数组
                if (length >= 2) {
                    return true;
                }
            } else {
                // 第一次遇到这个余数，记录索引
                remainderIndex[remainder] = i;
            }
        }

        return false;  // 没有找到满足条件的子数组
    }

    // ========================================================================
    // 方法2：暴力枚举（用于对比）
    // ========================================================================
    // 枚举所有长度 >= 2 的子数组，检查和是否为 k 的倍数
    // ========================================================================
    bool checkSubarraySumBruteForce(vector<int>& nums, int k) {
        int n = nums.size();

        // 枚举起点 i
        for (int i = 0; i < n; i++) {
            int sum = 0;
            // 枚举终点 j（至少距离 i 为 1，保证长度 >= 2）
            for (int j = i; j < n; j++) {
                sum += nums[j];
                // 长度至少为 2
                if (j - i + 1 >= 2 && sum % k == 0) {
                    return true;
                }
            }
        }

        return false;
    }
};

// ============================================================================
// 示例推演：展示前缀和、余数和哈希表的变化过程
// ============================================================================
void demonstrateAlgorithm() {
    cout << "============================================" << endl;
    cout << "示例推演：nums = [23, 2, 4, 6, 7], k = 6" << endl;
    cout << "============================================" << endl;

    vector<int> nums = {23, 2, 4, 6, 7};
    int k = 6;

    unordered_map<int, int> remainderIndex;
    remainderIndex[0] = -1;

    int prefixSum = 0;

    cout << "初始状态：" << endl;
    cout << "  哈希表: {0: -1}" << endl << endl;

    for (int i = 0; i < nums.size(); i++) {
        prefixSum += nums[i];
        int remainder = prefixSum % k;

        cout << "步骤 " << (i + 1) << ": 处理 nums[" << i << "] = " << nums[i] << endl;
        cout << "  当前前缀和: " << prefixSum << endl;
        cout << "  余数: " << remainder << " (" << prefixSum << " % " << k << ")" << endl;

        if (remainderIndex.find(remainder) != remainderIndex.end()) {
            int length = i - remainderIndex[remainder];
            cout << "  找到相同余数，之前在索引 " << remainderIndex[remainder] << endl;
            cout << "  子数组长度: " << length << " (索引 " << remainderIndex[remainder] + 1
                 << " 到 " << i << ")" << endl;

            if (length >= 2) {
                cout << "  ✓ 长度 >= 2，找到满足条件的子数组！" << endl;
                cout << "  子数组: [";
                for (int j = remainderIndex[remainder] + 1; j <= i; j++) {
                    if (j > remainderIndex[remainder] + 1) cout << ", ";
                    cout << nums[j];
                }
                cout << "]" << endl;
                break;
            } else {
                cout << "  ✗ 长度 < 2，不满足条件" << endl;
            }
        } else {
            cout << "  未找到相同余数，记录索引 " << i << endl;
            remainderIndex[remainder] = i;
        }

        cout << "  当前哈希表: {";
        bool first = true;
        for (auto& p : remainderIndex) {
            if (!first) cout << ", ";
            cout << p.first << ": " << p.second;
            first = false;
        }
        cout << "}" << endl << endl;
    }

    cout << "============================================" << endl << endl;
}

