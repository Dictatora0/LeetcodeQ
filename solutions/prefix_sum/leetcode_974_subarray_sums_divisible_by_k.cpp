/*
 * LeetCode 974: 和可被 K 整除的子数组 (Subarray Sums Divisible by K)
 * 难度：Medium
 *
 * ============================================================================
 * 题目概述
 * ============================================================================
 * 给定一个整数数组 nums 和一个整数 k，返回其中元素之和可被 k 整除的非空子数组的数目。
 *
 * 关键约束：
 * - 1 <= nums.length <= 3 * 10^4
 * - -10^4 <= nums[i] <= 10^4
 * - 2 <= k <= 10^4
 * - 数组中可能包含负数
 *
 * ============================================================================
 * 示例
 * ============================================================================
 * 示例 1:
 * 输入：nums = [4,5,0,-2,-3,1], k = 5
 * 输出：7
 * 解释：有 7 个子数组满足其元素之和可被 k = 5 整除：
 * [4, 5, 0, -2, -3, 1], [5], [5, 0], [5, 0, -2, -3], [0], [0, -2, -3], [-2, -3]
 *
 * 示例 2:
 * 输入：nums = [5], k = 9
 * 输出：0
 *
 * ============================================================================
 * 核心思想：前缀和 + 哈希表 + 同余定理
 * ============================================================================
 *
 * 1. 同余定理基础：
 *    如果 (a - b) % k == 0，则 a % k == b % k
 *    即：a 和 b 对 k 同余
 *
 * 2. 问题转换：
 *    要找和可被 k 整除的子数组 [i, j]，即：
 *    (prefixSum[j+1] - prefixSum[i]) % k == 0
 *    => prefixSum[j+1] % k == prefixSum[i] % k
 *
 * 3. 算法流程：
 *    - 计算当前前缀和的余数 remainder = prefixSum % k
 *    - 查找哈希表中余数为 remainder 的前缀和个数
 *    - 将当前余数加入哈希表
 *
 * 4. 负数取模的处理：
 *    C++ 中负数取模结果可能为负，需要调整：
 *    remainder = ((prefixSum % k) + k) % k
 *
 * ============================================================================
 * 同余定理详解
 * ============================================================================
 *
 * 定理：如果两个数对 k 的余数相同，它们的差就能被 k 整除
 *
 * 证明：
 * 设 a = k * q1 + r, b = k * q2 + r （r 是余数）
 * 则 a - b = k * (q1 - q2)
 * 所以 (a - b) % k == 0
 *
 * 应用到本题：
 * prefixSum[j] % k == prefixSum[i] % k
 * => (prefixSum[j] - prefixSum[i]) % k == 0
 * => sum(i, j-1) % k == 0
 *
 * ============================================================================
 * 负数取模处理
 * ============================================================================
 *
 * 问题：C++ 中 -7 % 5 = -2，而不是 3
 * 解决：remainder = ((num % k) + k) % k
 *
 * 示例：
 * -7 % 5 = -2
 * ((-7 % 5) + 5) % 5 = (-2 + 5) % 5 = 3 % 5 = 3 ✓
 *
 * 为什么要这样处理：
 * - 保证余数在 [0, k-1] 范围内
 * - 统一正负数的余数表示
 * - 确保同余关系的正确性
 *
 * ============================================================================
 * 前缀和原理图解
 * ============================================================================
 *
 * 数组：     [4,  5,  0,  -2,  -3,  1]    k = 5
 * 索引：      0   1   2   3    4    5
 *
 * 前缀和：
 * prefixSum[0] = 0,  余数 = 0
 * prefixSum[1] = 4,  余数 = 4
 * prefixSum[2] = 9,  余数 = 4  <- 与 prefixSum[1] 同余
 * prefixSum[3] = 9,  余数 = 4  <- 与 prefixSum[1], [2] 同余
 * prefixSum[4] = 7,  余数 = 2
 * prefixSum[5] = 4,  余数 = 4  <- 与 prefixSum[1], [2], [3] 同余
 * prefixSum[6] = 5,  余数 = 0  <- 与 prefixSum[0] 同余
 *
 * 余数为 4 的前缀和有 4 个：可以组成 C(4,2) = 6 个子数组
 * 余数为 0 的前缀和有 2 个：可以组成 C(2,2) = 1 个子数组
 * 总共：6 + 1 = 7 个子数组
 *
 * ============================================================================
 * 复杂度分析
 * ============================================================================
 *
 * 方法1：前缀和 + 哈希表 + 同余（最优）
 * 时间复杂度：O(n) - 遍历一次数组
 * 空间复杂度：O(min(n, k)) - 哈希表最多存储 k 个不同的余数
 *
 * 方法2：暴力枚举
 * 时间复杂度：O(n²) - 双重循环
 * 空间复杂度：O(1)
 *
 * ============================================================================
 * 特殊情况处理
 * ============================================================================
 *
 * 1. 负数取模：使用 ((num % k) + k) % k 确保余数非负
 * 2. 初始化：remainderCount[0] = 1，处理从索引 0 开始的子数组
 * 3. 余数范围：余数在 [0, k-1] 范围内
 * 4. 重复余数：使用哈希表记录每个余数出现的次数
 *
 */

#include <iostream>
#include <vector>
#include <unordered_map>
using namespace std;

class Solution {
public:
    // ========================================================================
    // 方法1：前缀和 + 哈希表 + 同余定理（最优解法）
    // ========================================================================
    // 核心思想：
    // 1. 如果两个前缀和对 k 的余数相同，它们的差就能被 k 整除
    // 2. 使用哈希表记录每个余数出现的次数
    // 3. 对于当前余数 r，之前出现过 count[r] 次，就能组成 count[r] 个子数组
    // ========================================================================
    int subarraysDivByK(vector<int>& nums, int k) {
        // 哈希表：key = 余数，value = 该余数出现的次数
        unordered_map<int, int> remainderCount;

        // 初始化：余数为 0 出现 1 次（表示空数组）
        // 这是为了处理从索引 0 开始的子数组
        remainderCount[0] = 1;

        int prefixSum = 0;  // 当前前缀和
        int count = 0;      // 和可被 k 整除的子数组个数

        // 遍历数组，计算前缀和
        for (int i = 0; i < nums.size(); i++) {
            // 更新前缀和
            prefixSum += nums[i];

            // 计算余数（处理负数）
            // C++ 中负数取模可能为负，需要调整到 [0, k-1] 范围
            int remainder = ((prefixSum % k) + k) % k;

            // 查找之前是否出现过相同的余数
            // 如果出现过 n 次，说明可以组成 n 个和可被 k 整除的子数组
            if (remainderCount.find(remainder) != remainderCount.end()) {
                count += remainderCount[remainder];
            }

            // 将当前余数加入哈希表（或更新计数）
            remainderCount[remainder]++;
        }

        return count;
    }

    // ========================================================================
    // 方法2：暴力枚举（用于对比）
    // ========================================================================
    // 枚举所有可能的子数组 [i, j]，计算和是否能被 k 整除
    // ========================================================================
    int subarraysDivByKBruteForce(vector<int>& nums, int k) {
        int count = 0;
        int n = nums.size();

        // 枚举起点 i
        for (int i = 0; i < n; i++) {
            int sum = 0;
            // 枚举终点 j
            for (int j = i; j < n; j++) {
                sum += nums[j];
                if (sum % k == 0) {
                    count++;
                }
            }
        }

        return count;
    }
};

// ============================================================================
// 示例推演：展示前缀和、余数和哈希表的变化过程
// ============================================================================
void demonstrateAlgorithm() {
    cout << "============================================" << endl;
    cout << "示例推演：nums = [4, 5, 0, -2, -3, 1], k = 5" << endl;
    cout << "============================================" << endl;

    vector<int> nums = {4, 5, 0, -2, -3, 1};
    int k = 5;

    unordered_map<int, int> remainderCount;
    remainderCount[0] = 1;

    int prefixSum = 0;
    int count = 0;

    cout << "初始状态：" << endl;
    cout << "  哈希表: {0: 1}" << endl;
    cout << "  count = 0" << endl << endl;

    for (int i = 0; i < nums.size(); i++) {
        prefixSum += nums[i];
        int remainder = ((prefixSum % k) + k) % k;

        cout << "步骤 " << (i + 1) << ": 处理 nums[" << i << "] = " << nums[i] << endl;
        cout << "  当前前缀和: " << prefixSum << endl;
        cout << "  余数: " << remainder << " (" << prefixSum << " % " << k << ")" << endl;

        if (remainderCount.find(remainder) != remainderCount.end()) {
            cout << "  找到 " << remainderCount[remainder] << " 个相同余数的前缀和" << endl;
            count += remainderCount[remainder];
        } else {
            cout << "  未找到相同余数" << endl;
        }

        remainderCount[remainder]++;

        cout << "  更新后 count = " << count << endl;
        cout << "  更新后哈希表: {";
        bool first = true;
        for (auto& p : remainderCount) {
            if (!first) cout << ", ";
            cout << p.first << ": " << p.second;
            first = false;
        }
        cout << "}" << endl << endl;
    }

    cout << "最终结果：" << count << " 个和可被 " << k << " 整除的子数组" << endl;
    cout << "============================================" << endl << endl;
}

// ============================================================================
// 负数取模演示
// ============================================================================
void demonstrateNegativeModulo() {
    cout << "============================================" << endl;
    cout << "负数取模处理演示" << endl;
    cout << "============================================" << endl;

    int k = 5;
    vector<int> testValues = {7, -7, 12, -12, 3, -3};

    cout << "k = " << k << endl << endl;

    for (int num : testValues) {
        int wrongRemainder = num % k;
        int correctRemainder = ((num % k) + k) % k;

        cout << "num = " << num << endl;
        cout << "  直接取模: " << num << " % " << k << " = " << wrongRemainder << endl;
        cout << "  正确处理: ((" << num << " % " << k << ") + " << k << ") % " << k
             << " = " << correctRemainder << endl;
        cout << endl;
    }

    cout << "结论：负数取模需要调整到 [0, k-1] 范围" << endl;
    cout << "============================================" << endl << endl;
}

