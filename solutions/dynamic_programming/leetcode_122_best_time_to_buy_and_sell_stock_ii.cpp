/*
示例推演 (Example Walkthrough)
-----------------------------
输入：prices = [7,1,5,3,6,4]

方法一（贪心）：
i=1: 1 < 7，不交易
i=2: 5 > 1，交易，利润 += 4，总利润 = 4
i=3: 3 < 5，不交易
i=4: 6 > 3，交易，利润 += 3，总利润 = 7
i=5: 4 < 6，不交易
返回 7

方法二（DP）：
i=0: dp[0][0]=0, dp[0][1]=-7
i=1: dp[1][0]=0, dp[1][1]=-1
i=2: dp[2][0]=4, dp[2][1]=-1
i=3: dp[3][0]=4, dp[3][1]=1
i=4: dp[4][0]=7, dp[4][1]=1
i=5: dp[5][0]=7, dp[5][1]=3
返回 7

DP 数组变化：
天数  价格  不持有  持有   说明
 0     7      0     -7    初始买入
 1     1      0     -1    更低价买入
 2     5      4     -1    卖出获利 4
 3     3      4      1    再次买入（利润 4-3=1）
 4     6      7      1    卖出获利 7
 5     4      7      3    再次买入（利润 7-4=3）

注意：持有状态的值表示"当前持有股票时的最大利润"，可以为正数！
*/

/*
方法对比 (Approach Comparison)
----------------------------
方法一：贪心 ⭐ 推荐
- 时间复杂度：O(n)
- 空间复杂度：O(1)
- 优点：代码简洁，直观易懂，效率最高
- 缺点：需要理解贪心的正确性

方法二：DP（数组）
- 时间复杂度：O(n)
- 空间复杂度：O(n)
- 优点：状态转移清晰，通用性强
- 缺点：空间未优化

方法三：DP（空间优化）
- 时间复杂度：O(n)
- 空间复杂度：O(1)
- 优点：空间最优，适合理解 DP
- 缺点：相比贪心稍复杂

推荐使用方法一（贪心），但理解 DP 方法对学习股票问题框架很重要。
*/

/*
关键要点 (Key Points)
-------------------
1. 贪心策略：累加所有上涨段
2. 贪心正确性：连续上涨可以拆分成多次交易
3. DP 状态：持有状态可以为正数（表示之前已有利润）
4. 与 121 题的区别：买入时考虑之前的利润
5. 空间优化：滚动变量代替数组
*/

/*
常见错误 (Common Mistakes)
------------------------
1. 误以为要找所有波峰波谷：不需要，直接累加上涨即可
2. 担心同时持有多只股票：贪心保证了先卖后买
3. DP 状态更新顺序错误：要用临时变量保存
*/

