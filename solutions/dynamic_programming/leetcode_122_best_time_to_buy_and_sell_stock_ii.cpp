/*
LeetCode 122. 买卖股票的最佳时机 II (Best Time to Buy and Sell Stock II)

题目概述 (Problem Summary)
-------------------------
给定一个数组 prices，其中 prices[i] 表示某支股票第 i 天的价格。

设计一个算法来计算你所能获取的最大利润。你可以尽可能地完成更多的交易（多次买卖一支股票）。

注意：你不能同时参与多笔交易（你必须在再次购买前出售掉之前的股票）。

关键约束 (Key Constraints)
-------------------------
- 1 <= prices.length <= 3 * 10^4
- 0 <= prices[i] <= 10^4
- 可以多次买卖，但不能同时持有多只股票

示例 (Example)
-------------
输入：prices = [7,1,5,3,6,4]
输出：7
解释：在第 2 天（股票价格 = 1）的时候买入，在第 3 天（股票价格 = 5）的时候卖出，利润 = 5-1 = 4。
     随后，在第 4 天（股票价格 = 3）的时候买入，在第 5 天（股票价格 = 6）的时候卖出，利润 = 6-3 = 3。
     总利润 = 4 + 3 = 7。

输入：prices = [1,2,3,4,5]
输出：4
解释：在第 1 天（股票价格 = 1）的时候买入，在第 5 天（股票价格 = 5）的时候卖出，利润 = 5-1 = 4。
     注意你不能在第 1 天和第 2 天接连购买股票，之后再将它们卖出。
     因为这样属于同时参与了多笔交易，你必须在再次购买前出售掉之前的股票。

输入：prices = [7,6,4,3,1]
输出：0
解释：在这种情况下，没有交易完成，所以最大利润为 0。

算法思路 (Algorithm Thinking)
----------------------------
这道题本质在解决什么问题？
- 可以多次买卖股票，求最大利润

关键观察：
1. 相比 121 题，这里可以进行无限次交易
2. 贪心策略：只要后一天价格高于前一天，就进行交易
3. 等价于：累加所有上涨的差值

状态机模型：
```
      买入          卖出
  ┌────────┐    ┌────────┐
  │        ▼    │        ▼
未持有 ──────> 持有 ──────> 未持有
  ▲                         │
  └─────────────────────────┘
         可以循环多次
```

DP 状态定义：
- dp[i][0]：第 i 天不持有股票的最大利润
- dp[i][1]：第 i 天持有股票的最大利润

状态转移方程：
- dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i])  // 保持不持有 或 今天卖出
- dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])  // 保持持有 或 今天买入

注意与 121 题的区别：
- 121 题：dp[i][1] = max(dp[i-1][1], -prices[i])       // 只能买入一次
- 122 题：dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])  // 可以多次买入

初始状态：
- dp[0][0] = 0
- dp[0][1] = -prices[0]

根据这些观察，可以得到几种典型解法：
1. 贪心（累加所有上涨）- 时间 O(n)，空间 O(1) ⭐ 最优
2. DP（状态机）- 时间 O(n)，空间 O(n)，可优化到 O(1)
*/

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

/*
方法一：贪心（累加所有上涨）(Approach 1: Greedy - Sum All Increases)

核心思想
--------
只要后一天价格高于前一天，就进行一次交易，累加所有利润。

为什么贪心是正确的？
- 假设价格序列为 [a, b, c]，其中 a < b < c
- 方案 1：在 a 买入，c 卖出，利润 = c - a
- 方案 2：在 a 买入 b 卖出，再在 b 买入 c 卖出，利润 = (b - a) + (c - b) = c - a
- 两种方案利润相同！
- 因此，可以把所有上涨段都拆分成单日交易

算法流程：
1. 遍历数组，如果 prices[i] > prices[i-1]，累加差值
2. 返回总利润

这是最直观、最优的解法！

复杂度分析
---------
时间复杂度：O(n)
- 遍历一次数组

空间复杂度：O(1)
- 只使用常数个变量
*/
class Solution_Greedy {
public:
    int maxProfit(vector<int>& prices) {
        int maxProfit = 0;

        // 累加所有上涨的差值
        for (int i = 1; i < prices.size(); i++) {
            if (prices[i] > prices[i-1]) {
                maxProfit += prices[i] - prices[i-1];
            }
        }

        return maxProfit;
    }
};

/*
方法二：DP（状态机）(Approach 2: Dynamic Programming with State Machine)

核心思想
--------
使用动态规划，定义两个状态：持有股票和不持有股票。

状态定义：
- dp[i][0]：第 i 天不持有股票的最大利润
- dp[i][1]：第 i 天持有股票的最大利润

状态转移：
- dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i])
  - dp[i-1][0]：昨天就不持有，今天继续不持有
  - dp[i-1][1] + prices[i]：昨天持有，今天卖出

- dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i])
  - dp[i-1][1]：昨天就持有，今天继续持有
  - dp[i-1][0] - prices[i]：昨天不持有，今天买入（关键：可以多次交易）

初始状态：
- dp[0][0] = 0
- dp[0][1] = -prices[0]

复杂度分析
---------
时间复杂度：O(n)
- 遍历一次数组

空间复杂度：O(n)
- dp 数组，可优化到 O(1)
*/
class Solution_DP {
public:
    int maxProfit(vector<int>& prices) {
        int n = prices.size();
        if (n == 0) return 0;

        // dp[i][0]：第 i 天不持有股票的最大利润
        // dp[i][1]：第 i 天持有股票的最大利润
        vector<vector<int>> dp(n, vector<int>(2));

        // 初始状态
        dp[0][0] = 0;
        dp[0][1] = -prices[0];

        // 状态转移
        for (int i = 1; i < n; i++) {
            // 不持有：保持不持有 或 今天卖出
            dp[i][0] = max(dp[i-1][0], dp[i-1][1] + prices[i]);

            // 持有：保持持有 或 今天买入（可以多次交易）
            dp[i][1] = max(dp[i-1][1], dp[i-1][0] - prices[i]);
        }

        // 最后一天不持有股票的利润最大
        return dp[n-1][0];
    }
};

/*
方法三：DP（空间优化）(Approach 3: DP with Space Optimization)

核心思想
--------
观察到 dp[i] 只依赖于 dp[i-1]，可以用两个变量代替数组。

复杂度分析
---------
时间复杂度：O(n)
- 遍历一次数组

空间复杂度：O(1)
- 只使用两个变量
*/
class Solution {
public:
    int maxProfit(vector<int>& prices) {
        int n = prices.size();
        if (n == 0) return 0;

        // 状态变量
        int hold = -prices[0];  // 持有股票的最大利润
        int notHold = 0;        // 不持有股票的最大利润

        for (int i = 1; i < n; i++) {
            // 更新状态（注意顺序）
            int newNotHold = max(notHold, hold + prices[i]);
            int newHold = max(hold, notHold - prices[i]);

            notHold = newNotHold;
            hold = newHold;
        }

        return notHold;
    }
};

/*
贪心正确性证明 (Proof of Greedy Correctness)
------------------------------------------
为什么累加所有上涨段是正确的？

数学证明：
假设价格序列为 [p0, p1, p2, ..., pn]，其中存在上涨段。

情况 1：连续上涨 [a, b, c]，其中 a < b < c
- 方案 A：a 买入，c 卖出，利润 = c - a
- 方案 B：a 买入 b 卖出，b 买入 c 卖出，利润 = (b-a) + (c-b) = c - a
- 结论：两种方案等价

情况 2：先涨后跌 [a, b, c]，其中 a < b > c
- 最优方案：a 买入，b 卖出，利润 = b - a
- 贪心方案：累加 (b-a)，跳过 (c-b)（因为 c < b）
- 结论：贪心正确

情况 3：先跌后涨 [a, b, c]，其中 a > b < c
- 最优方案：b 买入，c 卖出，利润 = c - b
- 贪心方案：跳过 (b-a)（因为 b < a），累加 (c-b)
- 结论：贪心正确

因此，贪心策略"累加所有上涨段"是正确的！
*/

