/*
 * LeetCode 136: 只出现一次的数字 (Single Number)
 * 难度：Easy
 *
 * 题目描述：
 * 给定一个非空整数数组，除了某个元素只出现一次以外，其余每个元素均出现两次。
 * 找出那个只出现了一次的元素。
 *
 * 关键约束：
 * - 1 <= nums.length <= 3 * 10^4
 * - -3 * 10^4 <= nums[i] <= 3 * 10^4
 * - 除了某个元素只出现一次以外，其余每个元素均出现两次
 * - 要求：线性时间复杂度，不使用额外空间
 *
 * 示例 1：
 * 输入：nums = [2,2,1]
 * 输出：1
 *
 * 示例 2：
 * 输入：nums = [4,1,2,1,2]
 * 输出：4
 *
 * 示例 3：
 * 输入：nums = [1]
 * 输出：1
 *
 * ============================================================================
 *
 * 核心思路：异或运算的性质
 *
 * 异或运算（XOR）的重要性质：
 * 1. 交换律：a ^ b = b ^ a
 * 2. 结合律：(a ^ b) ^ c = a ^ (b ^ c)
 * 3. 自反性：a ^ a = 0（任何数和自己异或等于0）
 * 4. 恒等性：a ^ 0 = a（任何数和0异或等于自己）
 *
 * 原理图解：
 *
 * 例如：nums = [4, 1, 2, 1, 2]
 *
 * 二进制表示：
 * 4: 0100
 * 1: 0001
 * 2: 0010
 * 1: 0001
 * 2: 0010
 *
 * 异或过程：
 * 0000 (初始值)
 * ^ 0100 = 0100 (4)
 * ^ 0001 = 0101 (4^1)
 * ^ 0010 = 0111 (4^1^2)
 * ^ 0001 = 0110 (4^1^2^1 = 4^2，因为1^1=0)
 * ^ 0010 = 0100 (4^2^2 = 4，因为2^2=0)
 *
 * 结果：4
 *
 * 为什么有效？
 * - 所有出现两次的数字异或后都变成0
 * - 0与只出现一次的数字异或，结果就是那个数字本身
 *
 * ============================================================================
 */

#include <iostream>
#include <vector>
#include <unordered_map>
using namespace std;

class Solution {
public:
    // ========================================================================
    // 方法1：异或运算（最优解）
    // 时间复杂度：O(n) - 遍历数组一次
    // 空间复杂度：O(1) - 只使用一个变量
    // ========================================================================
    int singleNumber(vector<int>& nums) {
        int result = 0;

        // 将所有数字进行异或运算
        // 相同的数字异或后为0，最后剩下的就是只出现一次的数字
        for (int num : nums) {
            result ^= num;
        }

        return result;
    }

    // ========================================================================
    // 方法2：哈希表
    // 时间复杂度：O(n) - 遍历数组一次
    // 空间复杂度：O(n) - 需要哈希表存储
    // ========================================================================
    int singleNumberHash(vector<int>& nums) {
        unordered_map<int, int> count;

        // 统计每个数字出现的次数
        for (int num : nums) {
            count[num]++;
        }

        // 找出只出现一次的数字
        for (auto& [num, cnt] : count) {
            if (cnt == 1) {
                return num;
            }
        }

        return -1; // 不会执行到这里
    }
};

/*
 * ============================================================================
 * 位运算技巧总结
 * ============================================================================
 *
 * 1. 异或运算的核心性质：
 *    - a ^ a = 0：相同数字异或为0
 *    - a ^ 0 = a：任何数与0异或等于自己
 *    - 异或满足交换律和结合律
 *
 * 2. 常见应用场景：
 *    - 找出数组中唯一不重复的元素
 *    - 交换两个变量（不用临时变量）：a ^= b; b ^= a; a ^= b;
 *    - 判断两个数是否相等：a ^ b == 0
 *
 * 3. 时间复杂度优势：
 *    - 位运算是CPU级别的操作，速度极快
 *    - 不需要额外空间，满足题目要求
 *
 * ============================================================================
 * 方法对比
 * ============================================================================
 *
 * | 方法     | 时间复杂度 | 空间复杂度 | 优点               | 缺点           |
 * |----------|------------|------------|--------------------|----------------|
 * | 异或运算 | O(n)       | O(1)       | 最优，满足所有要求 | 需要理解原理   |
 * | 哈希表   | O(n)       | O(n)       | 直观易懂           | 需要额外空间   |
 *
 * ============================================================================
 */

// 测试用例
int main() {
    Solution solution;

    // 测试用例1：基本情况
    vector<int> nums1 = {2, 2, 1};
    cout << "测试用例1: [2,2,1]" << endl;
    cout << "结果: " << solution.singleNumber(nums1) << endl;
    cout << "期望: 1" << endl;
    cout << endl;

    // 测试用例2：多个重复数字
    vector<int> nums2 = {4, 1, 2, 1, 2};
    cout << "测试用例2: [4,1,2,1,2]" << endl;
    cout << "结果: " << solution.singleNumber(nums2) << endl;
    cout << "期望: 4" << endl;
    cout << endl;

    // 测试用例3：只有一个元素
    vector<int> nums3 = {1};
    cout << "测试用例3: [1]" << endl;
    cout << "结果: " << solution.singleNumber(nums3) << endl;
    cout << "期望: 1" << endl;
    cout << endl;

    // 测试用例4：负数
    vector<int> nums4 = {-1, -1, -2};
    cout << "测试用例4: [-1,-1,-2]" << endl;
    cout << "结果: " << solution.singleNumber(nums4) << endl;
    cout << "期望: -2" << endl;
    cout << endl;

    // 测试用例5：大数组
    vector<int> nums5 = {1, 3, 1, 3, 5, 7, 5, 9, 7};
    cout << "测试用例5: [1,3,1,3,5,7,5,9,7]" << endl;
    cout << "结果: " << solution.singleNumber(nums5) << endl;
    cout << "期望: 9" << endl;
    cout << endl;

    // 演示异或过程
    cout << "========================================" << endl;
    cout << "异或过程演示 (nums = [4,1,2,1,2]):" << endl;
    cout << "========================================" << endl;
    vector<int> demo = {4, 1, 2, 1, 2};
    int result = 0;
    for (int num : demo) {
        int prev = result;
        result ^= num;
        cout << prev << " ^ " << num << " = " << result << endl;
    }
    cout << "最终结果: " << result << endl;

    return 0;
}
