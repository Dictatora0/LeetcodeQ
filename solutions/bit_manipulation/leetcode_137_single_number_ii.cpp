/*
 * LeetCode 137: 只出现一次的数字 II (Single Number II)
 * 难度：Medium
 *
 * 题目描述：
 * 给定一个整数数组 nums，除某个元素仅出现一次外，其余每个元素都恰出现三次。
 * 请找出并返回那个只出现了一次的元素。
 *
 * 关键约束：
 * - 1 <= nums.length <= 3 * 10^4
 * - -2^31 <= nums[i] <= 2^31 - 1
 * - nums 中，除某个元素仅出现一次外，其余每个元素都恰出现三次
 * - 要求：线性时间复杂度，常数空间
 *
 * 示例 1：
 * 输入：nums = [2,2,3,2]
 * 输出：3
 *
 * 示例 2：
 * 输入：nums = [0,1,0,1,0,1,99]
 * 输出：99
 *
 * ============================================================================
 *
 * 核心思路：按位统计
 *
 * 关键观察：
 * - 如果一个数字出现3次，那么它的每一位（二进制）也出现3次
 * - 如果把所有数字的每一位加起来，对3取模，剩下的就是只出现一次的数字的那一位
 *
 * 原理图解：
 *
 * 例如：nums = [2, 2, 3, 2]
 *
 * 二进制表示：
 * 2: 010
 * 2: 010
 * 3: 011
 * 2: 010
 *
 * 按位统计：
 * 第0位（最右）：0+0+1+0 = 1，1 % 3 = 1
 * 第1位：       1+1+1+1 = 4，4 % 3 = 1
 * 第2位：       0+0+0+0 = 0，0 % 3 = 0
 *
 * 结果：011 (二进制) = 3 (十进制)
 *
 * 为什么有效？
 * - 出现3次的数字，每一位的和都是3的倍数
 * - 对3取模后，这些位都变成0
 * - 只出现1次的数字的位保留下来
 *
 * ============================================================================
 */

#include <iostream>
#include <vector>
#include <unordered_map>
using namespace std;

class Solution {
public:
    // ========================================================================
    // 方法1：按位统计（最优解）
    // 时间复杂度：O(n) - 遍历数组，对每个数字处理32位
    // 空间复杂度：O(1) - 只使用固定大小的数组
    // ========================================================================
    int singleNumber(vector<int>& nums) {
        int result = 0;

        // 对于32位整数，检查每一位
        for (int i = 0; i < 32; i++) {
            int bitSum = 0;

            // 统计所有数字在第i位上1的个数
            for (int num : nums) {
                bitSum += (num >> i) & 1;
            }

            // 如果该位的和不是3的倍数，说明只出现一次的数字在这一位是1
            if (bitSum % 3 != 0) {
                result |= (1 << i);
            }
        }

        return result;
    }

    // ========================================================================
    // 方法2：有限状态机（数字电路方法）
    // 时间复杂度：O(n)
    // 空间复杂度：O(1)
    //
    // 使用两个变量 ones 和 twos 来记录每一位出现的次数：
    // - ones: 记录出现1次的位
    // - twos: 记录出现2次的位
    // - 出现3次时，该位在 ones 和 twos 中都清零
    // ========================================================================
    int singleNumberStateMachine(vector<int>& nums) {
        int ones = 0, twos = 0;

        for (int num : nums) {
            // twos 记录在 ones 中出现过，现在又出现的位
            twos |= ones & num;

            // ones 记录当前出现的位
            ones ^= num;

            // 找出在 ones 和 twos 中都为1的位（出现了3次）
            int threes = ones & twos;

            // 将出现3次的位从 ones 和 twos 中清除
            ones &= ~threes;
            twos &= ~threes;
        }

        return ones;
    }

    // ========================================================================
    // 方法3：哈希表
    // 时间复杂度：O(n)
    // 空间复杂度：O(n)
    // ========================================================================
    int singleNumberHash(vector<int>& nums) {
        unordered_map<int, int> count;

        for (int num : nums) {
            count[num]++;
        }

        for (auto& [num, cnt] : count) {
            if (cnt == 1) {
                return num;
            }
        }

        return -1;
    }
};

/*
 * ============================================================================
 * 位运算技巧总结
 * ============================================================================
 *
 * 1. 按位统计模板：
 *    - 适用于"除了一个数出现k次，其他数都出现m次"的问题
 *    - 统计每一位上1的个数，对m取模
 *    - 剩下的就是出现k次的数字
 *
 * 2. 常用位运算操作：
 *    - (num >> i) & 1：获取num的第i位
 *    - result |= (1 << i)：将result的第i位设为1
 *    - num & ~mask：清除num中mask为1的位
 *
 * 3. 有限状态机方法：
 *    - 使用多个变量记录不同的状态
 *    - 通过位运算实现状态转移
 *    - 更高效但更难理解
 *
 * 4. 负数处理：
 *    - C++中负数使用补码表示
 *    - 右移操作对负数是算术右移（保持符号位）
 *    - 本算法对负数同样有效
 *
 * ============================================================================
 * 方法对比
 * ============================================================================
 *
 * | 方法         | 时间复杂度 | 空间复杂度 | 优点               | 缺点           |
 * |--------------|------------|------------|--------------------|----------------|
 * | 按位统计     | O(n)       | O(1)       | 易理解，通用性强   | 需要遍历32位   |
 * | 有限状态机   | O(n)       | O(1)       | 最快，一次遍历     | 难以理解       |
 * | 哈希表       | O(n)       | O(n)       | 直观               | 需要额外空间   |
 *
 * ============================================================================
 */

// 辅助函数：打印二进制表示
void printBinary(int num, const string& label) {
    cout << label << num << " (二进制: ";
    for (int i = 31; i >= 0; i--) {
        cout << ((num >> i) & 1);
        if (i % 4 == 0 && i > 0) cout << " ";
    }
    cout << ")" << endl;
}

// 测试用例
int main() {
    Solution solution;

    // 测试用例1：基本情况
    vector<int> nums1 = {2, 2, 3, 2};
    cout << "测试用例1: [2,2,3,2]" << endl;
    cout << "结果: " << solution.singleNumber(nums1) << endl;
    cout << "期望: 3" << endl;
    cout << endl;

    // 测试用例2：包含0
    vector<int> nums2 = {0, 1, 0, 1, 0, 1, 99};
    cout << "测试用例2: [0,1,0,1,0,1,99]" << endl;
    cout << "结果: " << solution.singleNumber(nums2) << endl;
    cout << "期望: 99" << endl;
    cout << endl;

    // 测试用例3：负数
    vector<int> nums3 = {-2, -2, 1, -2};
    cout << "测试用例3: [-2,-2,1,-2]" << endl;
    cout << "结果: " << solution.singleNumber(nums3) << endl;
    cout << "期望: 1" << endl;
    cout << endl;

    // 测试用例4：只有一个元素
    vector<int> nums4 = {1};
    cout << "测试用例4: [1]" << endl;
    cout << "结果: " << solution.singleNumber(nums4) << endl;
    cout << "期望: 1" << endl;
    cout << endl;

    // 测试用例5：大数
    vector<int> nums5 = {43, 16, 43, 43, 16, 16};
    cout << "测试用例5: [43,16,43,43,16,16]" << endl;
    cout << "结果: " << solution.singleNumber(nums5) << endl;
    cout << "期望: 0" << endl;
    cout << endl;

    // 演示按位统计过程
    cout << "========================================" << endl;
    cout << "按位统计演示 (nums = [2,2,3,2]):" << endl;
    cout << "========================================" << endl;
    vector<int> demo = {2, 2, 3, 2};

    cout << "二进制表示：" << endl;
    for (int num : demo) {
        printBinary(num, "  ");
    }
    cout << endl;

    cout << "按位统计：" << endl;
    for (int i = 0; i < 4; i++) {
        int bitSum = 0;
        for (int num : demo) {
            bitSum += (num >> i) & 1;
        }
        cout << "第" << i << "位: " << bitSum << " % 3 = " << (bitSum % 3) << endl;
    }

    cout << "\n最终结果: 3 (二进制: 0011)" << endl;

    // 对比三种方法
    cout << "\n========================================" << endl;
    cout << "方法对比：" << endl;
    cout << "========================================" << endl;
    vector<int> test = {5, 5, 5, 7};
    cout << "测试数组: [5,5,5,7]" << endl;
    cout << "方法1（按位统计）: " << solution.singleNumber(test) << endl;
    cout << "方法2（状态机）: " << solution.singleNumberStateMachine(test) << endl;
    cout << "方法3（哈希表）: " << solution.singleNumberHash(test) << endl;

    return 0;
}
