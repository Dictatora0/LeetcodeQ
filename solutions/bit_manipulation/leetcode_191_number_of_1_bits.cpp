/*
 * LeetCode 191: 位1的个数 (Number of 1 Bits)
 * 难度：Easy
 *
 * 题目描述：
 * 编写一个函数，输入是一个无符号整数（以二进制串的形式），返回其二进制表达式中
 * 数字位数为 '1' 的个数（也被称为汉明重量）。
 *
 * 关键约束：
 * - 输入是一个长度为 32 的二进制串
 * - 输入必须是长度为 32 的二进制串（即无符号整数）
 *
 * 示例 1：
 * 输入：n = 00000000000000000000000000001011
 * 输出：3
 * 解释：输入的二进制串有三个 '1'
 *
 * 示例 2：
 * 输入：n = 00000000000000000000000010000000
 * 输出：1
 *
 * 示例 3：
 * 输入：n = 11111111111111111111111111111101
 * 输出：31
 *
 * ============================================================================
 *
 * 核心思路：n & (n-1) 技巧
 *
 * 关键观察：
 * - n & (n-1) 可以消除 n 最右边的一个 1
 * - 重复这个操作直到 n 变成 0，操作次数就是 1 的个数
 *
 * 原理图解：
 *
 * 例如：n = 1011 (十进制11)
 *
 * 第1次：
 * n     = 1011
 * n-1   = 1010
 * n&(n-1) = 1010  (消除了最右边的1)
 *
 * 第2次：
 * n     = 1010
 * n-1   = 1001
 * n&(n-1) = 1000  (消除了最右边的1)
 *
 * 第3次：
 * n     = 1000
 * n-1   = 0111
 * n&(n-1) = 0000  (消除了最右边的1)
 *
 * 总共操作3次，所以有3个1
 *
 * 为什么 n & (n-1) 能消除最右边的1？
 * - n-1 会将最右边的1变成0，并将其右边的所有0变成1
 * - 与运算后，最右边的1及其右边的位都变成0
 * - 例如：1011 - 1 = 1010，1011 & 1010 = 1010
 *
 * ============================================================================
 */

#include <iostream>
using namespace std;

class Solution {
public:
    // ========================================================================
    // 方法1：n & (n-1) 技巧（最优解）
    // 时间复杂度：O(k) - k是1的个数，最多32次
    // 空间复杂度：O(1)
    // ========================================================================
    int hammingWeight(uint32_t n) {
        int count = 0;

        // 每次消除最右边的1
        while (n != 0) {
            n &= (n - 1);
            count++;
        }

        return count;
    }

    // ========================================================================
    // 方法2：逐位检查
    // 时间复杂度：O(32) - 固定检查32位
    // 空间复杂度：O(1)
    // ========================================================================
    int hammingWeightLoop(uint32_t n) {
        int count = 0;

        // 检查每一位
        for (int i = 0; i < 32; i++) {
            if (n & (1 << i)) {
                count++;
            }
        }

        return count;
    }

    // ========================================================================
    // 方法3：右移检查
    // 时间复杂度：O(32)
    // 空间复杂度：O(1)
    // ========================================================================
    int hammingWeightShift(uint32_t n) {
        int count = 0;

        // 每次检查最低位，然后右移
        while (n != 0) {
            count += n & 1;
            n >>= 1;
        }

        return count;
    }

    // ========================================================================
    // 方法4：查表法（空间换时间）
    // 时间复杂度：O(1) - 最多4次查表
    // 空间复杂度：O(256) - 需要预先计算的表
    // ========================================================================
    int hammingWeightTable(uint32_t n) {
        // 预计算0-255每个数的1的个数
        static int table[256];
        static bool initialized = false;

        if (!initialized) {
            for (int i = 0; i < 256; i++) {
                table[i] = (i & 1) + table[i >> 1];
            }
            initialized = true;
        }

        // 将32位数分成4个8位，分别查表
        return table[n & 0xff] +
               table[(n >> 8) & 0xff] +
               table[(n >> 16) & 0xff] +
               table[(n >> 24) & 0xff];
    }
};

/*
 * ============================================================================
 * 位运算技巧总结
 * ============================================================================
 *
 * 1. n & (n-1) 的作用：
 *    - 消除 n 最右边的一个 1
 *    - 应用：计算1的个数、判断是否是2的幂（n & (n-1) == 0）
 *    - 时间复杂度与1的个数成正比，而不是位数
 *
 * 2. 检查第i位是否为1：
 *    - 方法1：n & (1 << i)
 *    - 方法2：(n >> i) & 1
 *
 * 3. 获取最右边的1：
 *    - n & (-n)
 *    - 应用：分组、树状数组
 *
 * 4. 清除最右边的1：
 *    - n & (n-1)
 *
 * 5. 设置第i位为1：
 *    - n | (1 << i)
 *
 * 6. 清除第i位：
 *    - n & ~(1 << i)
 *
 * 7. 翻转第i位：
 *    - n ^ (1 << i)
 *
 * ============================================================================
 * 方法对比
 * ============================================================================
 *
 * | 方法         | 时间复杂度 | 空间复杂度 | 优点               | 缺点           |
 * |--------------|------------|------------|--------------------|----------------|
 * | n&(n-1)      | O(k)       | O(1)       | 最优，只循环k次    | 需要理解原理   |
 * | 逐位检查     | O(32)      | O(1)       | 简单直观           | 固定32次循环   |
 * | 右移检查     | O(32)      | O(1)       | 简单               | 固定32次循环   |
 * | 查表法       | O(1)       | O(256)     | 最快               | 需要额外空间   |
 *
 * k = 1的个数
 *
 * ============================================================================
 */

// 辅助函数：打印二进制表示
void printBinary(uint32_t n) {
    cout << "二进制: ";
    for (int i = 31; i >= 0; i--) {
        cout << ((n >> i) & 1);
        if (i % 8 == 0 && i > 0) cout << " ";
    }
    cout << " (十进制: " << n << ")" << endl;
}

// 测试用例
int main() {
    Solution solution;

    // 测试用例1：基本情况
    uint32_t n1 = 0b00000000000000000000000000001011;
    cout << "测试用例1:" << endl;
    printBinary(n1);
    cout << "结果: " << solution.hammingWeight(n1) << endl;
    cout << "期望: 3" << endl;
    cout << endl;

    // 测试用例2：只有一个1
    uint32_t n2 = 0b00000000000000000000000010000000;
    cout << "测试用例2:" << endl;
    printBinary(n2);
    cout << "结果: " << solution.hammingWeight(n2) << endl;
    cout << "期望: 1" << endl;
    cout << endl;

    // 测试用例3：几乎全是1
    uint32_t n3 = 0b11111111111111111111111111111101;
    cout << "测试用例3:" << endl;
    printBinary(n3);
    cout << "结果: " << solution.hammingWeight(n3) << endl;
    cout << "期望: 31" << endl;
    cout << endl;

    // 测试用例4：全是1
    uint32_t n4 = 0xFFFFFFFF;
    cout << "测试用例4:" << endl;
    printBinary(n4);
    cout << "结果: " << solution.hammingWeight(n4) << endl;
    cout << "期望: 32" << endl;
    cout << endl;

    // 测试用例5：0
    uint32_t n5 = 0;
    cout << "测试用例5:" << endl;
    printBinary(n5);
    cout << "结果: " << solution.hammingWeight(n5) << endl;
    cout << "期望: 0" << endl;
    cout << endl;

    // 演示 n & (n-1) 过程
    cout << "========================================" << endl;
    cout << "n & (n-1) 演示 (n = 1011):" << endl;
    cout << "========================================" << endl;
    uint32_t demo = 0b1011;
    int step = 1;

    while (demo != 0) {
        cout << "第" << step << "步:" << endl;
        cout << "  n     = ";
        for (int i = 3; i >= 0; i--) cout << ((demo >> i) & 1);
        cout << endl;

        uint32_t prev = demo;
        demo &= (demo - 1);

        cout << "  n-1   = ";
        for (int i = 3; i >= 0; i--) cout << (((prev - 1) >> i) & 1);
        cout << endl;

        cout << "  n&(n-1) = ";
        for (int i = 3; i >= 0; i--) cout << ((demo >> i) & 1);
        cout << endl;
        cout << endl;

        step++;
    }

    cout << "总共操作 " << (step - 1) << " 次" << endl;
    cout << endl;

    // 对比四种方法
    cout << "========================================" << endl;
    cout << "方法对比 (n = 0xAAAAAAAA):" << endl;
    cout << "========================================" << endl;
    uint32_t test = 0xAAAAAAAA;
    printBinary(test);
    cout << "方法1 (n&(n-1)):  " << solution.hammingWeight(test) << endl;
    cout << "方法2 (逐位检查): " << solution.hammingWeightLoop(test) << endl;
    cout << "方法3 (右移检查): " << solution.hammingWeightShift(test) << endl;
    cout << "方法4 (查表法):   " << solution.hammingWeightTable(test) << endl;

    return 0;
}
