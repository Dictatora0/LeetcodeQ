/*
 * LeetCode 260: 只出现一次的数字 III (Single Number III)
 * 难度：Medium
 *
 * 题目描述：
 * 给定一个整数数组 nums，其中恰好有两个元素只出现一次，其余所有元素均出现两次。
 * 找出只出现一次的那两个元素。你可以按任意顺序返回答案。
 *
 * 关键约束：
 * - 2 <= nums.length <= 3 * 10^4
 * - -2^31 <= nums[i] <= 2^31 - 1
 * - 除两个元素只出现一次外，其他元素都出现两次
 * - 要求：线性时间复杂度，常数空间
 *
 * 示例 1：
 * 输入：nums = [1,2,1,3,2,5]
 * 输出：[3,5]
 * 解释：[5,3] 也是有效答案
 *
 * 示例 2：
 * 输入：nums = [-1,0]
 * 输出：[-1,0]
 *
 * 示例 3：
 * 输入：nums = [0,1]
 * 输出：[1,0]
 *
 * ============================================================================
 *
 * 核心思路：异或 + 分组
 *
 * 关键观察：
 * 1. 如果只有一个数字出现一次，直接异或所有数字即可得到结果
 * 2. 现在有两个数字（设为a和b）只出现一次，异或所有数字得到 a ^ b
 * 3. a ^ b 的结果中，为1的位表示a和b在该位上不同
 * 4. 利用这个不同的位，可以将数组分成两组：
 *    - 该位为1的一组（包含a）
 *    - 该位为0的一组（包含b）
 * 5. 分别对两组进行异或，就能得到a和b
 *
 * 原理图解：
 *
 * 例如：nums = [1, 2, 1, 3, 2, 5]
 *
 * 步骤1：异或所有数字
 * 1: 001
 * 2: 010
 * 1: 001
 * 3: 011
 * 2: 010
 * 5: 101
 * -------
 * 结果: 001 ^ 010 ^ 001 ^ 011 ^ 010 ^ 101 = 110 (即 3 ^ 5 = 6)
 *
 * 步骤2：找到最右边的1（任意一个1都可以）
 * 110 的最右边的1在第1位（从右往左，从0开始）
 * 使用技巧：xor & (-xor) = 010
 *
 * 步骤3：按第1位分组
 * 第1位为0：1(001), 1(001), 3(011), 5(101) -> 异或结果 = 5
 * 第1位为1：2(010), 2(010)                 -> 异或结果 = 3
 *
 * 等等，让我重新计算：
 * 第1位为1：2(010), 2(010)                 -> 异或结果 = 0？不对
 *
 * 让我重新分析：
 * xor = 3 ^ 5 = 011 ^ 101 = 110 (二进制) = 6
 * 最右边的1：110 & -110 = 110 & 11111010 = 010 (第1位)
 *
 * 按第1位分组：
 * 第1位为1：2(010), 2(010)                 -> 异或 = 0
 * 第1位为0：1(001), 1(001), 3(011), 5(101) -> 异或 = 011 ^ 101 = 110
 *
 * 不对，让我重新理解...实际上应该是：
 * 第1位为1的数：2, 2, 3 -> 异或 = 3
 * 第1位为0的数：1, 1, 5 -> 异或 = 5
 *
 * ============================================================================
 */

#include <iostream>
#include <vector>
#include <unordered_map>
using namespace std;

class Solution {
public:
    // ========================================================================
    // 方法1：异或 + 分组（最优解）
    // 时间复杂度：O(n) - 遍历数组两次
    // 空间复杂度：O(1) - 只使用常数个变量
    // ========================================================================
    vector<int> singleNumber(vector<int>& nums) {
        // 步骤1：异或所有数字，得到两个只出现一次的数字的异或结果
        int xorSum = 0;
        for (int num : nums) {
            xorSum ^= num;
        }

        // 步骤2：找到异或结果中任意一个为1的位
        // 这里使用 xorSum & (-xorSum) 获取最右边的1
        // 原理：-xorSum 是 xorSum 的补码，最右边的1保持不变，其他位取反
        int rightmostBit = xorSum & (-xorSum);

        // 步骤3：根据这一位将数组分成两组，分别异或
        int num1 = 0, num2 = 0;
        for (int num : nums) {
            if (num & rightmostBit) {
                // 该位为1的一组
                num1 ^= num;
            } else {
                // 该位为0的一组
                num2 ^= num;
            }
        }

        return {num1, num2};
    }

    // ========================================================================
    // 方法2：哈希表
    // 时间复杂度：O(n)
    // 空间复杂度：O(n)
    // ========================================================================
    vector<int> singleNumberHash(vector<int>& nums) {
        unordered_map<int, int> count;
        vector<int> result;

        for (int num : nums) {
            count[num]++;
        }

        for (auto& [num, cnt] : count) {
            if (cnt == 1) {
                result.push_back(num);
            }
        }

        return result;
    }
};

/*
 * ============================================================================
 * 位运算技巧总结
 * ============================================================================
 *
 * 1. 获取最右边的1：
 *    - x & (-x)：获取x最右边的1
 *    - 原理：-x 是 x 的补码（取反加1），最右边的1及其右边的0保持不变
 *    - 例如：x = 110100，-x = 001100，x & (-x) = 000100
 *
 * 2. 获取任意一个1：
 *    - x & (-x)：最右边的1
 *    - x & (x-1) ^ x：最右边的1（另一种方法）
 *    - 也可以用循环找到任意一个1
 *
 * 3. 分组技巧：
 *    - 利用某一位的不同将数组分成两组
 *    - 保证两个目标数字分别在不同组
 *    - 其他成对出现的数字在同一组内会被异或抵消
 *
 * 4. 负数的补码表示：
 *    - 正数的补码是其本身
 *    - 负数的补码是其绝对值按位取反后加1
 *    - 例如：6 = 0110，-6 = 1010
 *
 * ============================================================================
 * 方法对比
 * ============================================================================
 *
 * | 方法       | 时间复杂度 | 空间复杂度 | 优点               | 缺点           |
 * |------------|------------|------------|--------------------|----------------|
 * | 异或+分组  | O(n)       | O(1)       | 最优，满足所有要求 | 需要理解原理   |
 * | 哈希表     | O(n)       | O(n)       | 直观易懂           | 需要额外空间   |
 *
 * ============================================================================
 */

// 辅助函数：打印二进制表示
void printBinary(int num, const string& label) {
    cout << label << num << " (二进制: ";
    // 只打印低8位，便于观察
    for (int i = 7; i >= 0; i--) {
        cout << ((num >> i) & 1);
        if (i == 4) cout << " ";
    }
    cout << ")" << endl;
}

// 测试用例
int main() {
    Solution solution;

    // 测试用例1：基本情况
    vector<int> nums1 = {1, 2, 1, 3, 2, 5};
    cout << "测试用例1: [1,2,1,3,2,5]" << endl;
    vector<int> result1 = solution.singleNumber(nums1);
    cout << "结果: [" << result1[0] << "," << result1[1] << "]" << endl;
    cout << "期望: [3,5] 或 [5,3]" << endl;
    cout << endl;

    // 测试用例2：包含负数
    vector<int> nums2 = {-1, 0};
    cout << "测试用例2: [-1,0]" << endl;
    vector<int> result2 = solution.singleNumber(nums2);
    cout << "结果: [" << result2[0] << "," << result2[1] << "]" << endl;
    cout << "期望: [-1,0] 或 [0,-1]" << endl;
    cout << endl;

    // 测试用例3：最小情况
    vector<int> nums3 = {0, 1};
    cout << "测试用例3: [0,1]" << endl;
    vector<int> result3 = solution.singleNumber(nums3);
    cout << "结果: [" << result3[0] << "," << result3[1] << "]" << endl;
    cout << "期望: [0,1] 或 [1,0]" << endl;
    cout << endl;

    // 测试用例4：较大的数字
    vector<int> nums4 = {1, 2, 3, 4, 1, 2};
    cout << "测试用例4: [1,2,3,4,1,2]" << endl;
    vector<int> result4 = solution.singleNumber(nums4);
    cout << "结果: [" << result4[0] << "," << result4[1] << "]" << endl;
    cout << "期望: [3,4] 或 [4,3]" << endl;
    cout << endl;

    // 演示异或和分组过程
    cout << "========================================" << endl;
    cout << "异或和分组演示 (nums = [1,2,1,3,2,5]):" << endl;
    cout << "========================================" << endl;
    vector<int> demo = {1, 2, 1, 3, 2, 5};

    // 步骤1：计算异或和
    int xorSum = 0;
    cout << "步骤1：异或所有数字" << endl;
    for (int num : demo) {
        xorSum ^= num;
        printBinary(num, "  ");
    }
    printBinary(xorSum, "异或结果: ");
    cout << endl;

    // 步骤2：找到最右边的1
    int rightmostBit = xorSum & (-xorSum);
    cout << "步骤2：找到最右边的1" << endl;
    printBinary(xorSum, "xorSum:   ");
    printBinary(-xorSum, "-xorSum:  ");
    printBinary(rightmostBit, "最右边的1: ");
    cout << endl;

    // 步骤3：分组
    cout << "步骤3：按第" << __builtin_ctz(rightmostBit) << "位分组" << endl;
    cout << "（__builtin_ctz 返回末尾0的个数，即最右边1的位置）" << endl;
    cout << endl;

    vector<int> group1, group2;
    for (int num : demo) {
        if (num & rightmostBit) {
            group1.push_back(num);
        } else {
            group2.push_back(num);
        }
    }

    cout << "组1（该位为1）: ";
    for (int num : group1) cout << num << " ";
    cout << endl;

    cout << "组2（该位为0）: ";
    for (int num : group2) cout << num << " ";
    cout << endl;
    cout << endl;

    // 步骤4：分别异或
    int num1 = 0, num2 = 0;
    for (int num : group1) num1 ^= num;
    for (int num : group2) num2 ^= num;

    cout << "组1异或结果: " << num1 << endl;
    cout << "组2异或结果: " << num2 << endl;

    return 0;
}
