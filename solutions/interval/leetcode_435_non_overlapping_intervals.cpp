/*
LeetCode 435. 无重叠区间 (Non-overlapping Intervals)

题目概述 (Problem Summary)
-------------------------
给定一个区间的集合 intervals，其中 intervals[i] = [starti, endi]。
返回需要移除区间的最小数量，使剩余区间互不重叠。

注意：可以认为区间的终点总是大于它的起点。
区间 [1,2] 和 [2,3] 的边界相互"接触"，但没有相互重叠。

关键约束 (Key Constraints)
-------------------------
- 1 <= intervals.length <= 10^5
- intervals[i].length == 2
- -5 * 10^4 <= starti < endi <= 5 * 10^4

示例 (Example)
-------------
输入：intervals = [[1,2],[2,3],[3,4],[1,3]]
输出：1
解释：移除 [1,3] 后，剩余区间 [[1,2],[2,3],[3,4]] 互不重叠

输入：intervals = [[1,2],[1,2],[1,2]]
输出：2
解释：需要移除 2 个区间，只保留 1 个

输入：intervals = [[1,2],[2,3]]
输出：0
解释：不需要移除任何区间

算法思路 (Algorithm Thinking)
----------------------------
这道题本质在解决什么问题？
- 区间调度问题：在有限的资源下，选择最多的不重叠区间
- 移除最少的区间 = 保留最多的不重叠区间
- 答案 = 总区间数 - 最多不重叠区间数

关键观察：
1. 这是一个经典的贪心问题
2. 要保留最多的区间，应该优先选择"结束时间早"的区间
3. 结束时间越早，给后面的区间留的空间越大

为什么按结束时间排序？
- 如果按起始时间排序，无法做出最优选择
- 例如：[[1,10],[2,3],[4,5]]，按起始时间排序会选 [1,10]，但最优是选 [2,3] 和 [4,5]
- 按结束时间排序，总是选择结束最早的区间，这样给后续区间留的空间最大

贪心策略的正确性证明：
1. 假设最优解中第一个区间不是结束时间最早的区间 A，而是区间 B
2. 因为 A 的结束时间 <= B 的结束时间
3. 所以将 B 替换为 A，不会影响后续区间的选择
4. 因此，选择结束时间最早的区间一定是最优的

算法流程：
1. 按区间结束时间排序
2. 选择第一个区间（结束时间最早）
3. 遍历剩余区间：
   - 如果当前区间的起始时间 >= 上一个选择区间的结束时间（不重叠）
     - 选择当前区间
   - 否则（重叠）
     - 跳过当前区间（相当于移除）
4. 返回：总区间数 - 选择的区间数

示例推演（intervals = [[1,2],[2,3],[3,4],[1,3]]）：
按结束时间排序：[[1,2],[2,3],[1,3],[3,4]]

选择 [1,2]，end = 2
检查 [2,3]：2 >= 2（不重叠），选择，end = 3
检查 [1,3]：1 < 3（重叠），跳过
检查 [3,4]：3 >= 3（不重叠），选择，end = 4

选择了 3 个区间，移除 4 - 3 = 1 个区间
*/

#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

/*
方法一：贪心（按结束时间排序）(Approach 1: Greedy - Sort by End Time) - 最优解法

核心思想
--------
按区间结束时间排序，贪心地选择结束时间最早的不重叠区间。
选择的区间数量最多，移除的区间数量就最少。

算法流程：
1. 按区间结束时间排序
2. 初始化：选择第一个区间，记录其结束时间
3. 遍历剩余区间：
   - 如果当前区间的起始时间 >= 上一个选择区间的结束时间
     - 选择当前区间，更新结束时间
   - 否则，跳过当前区间
4. 返回：总区间数 - 选择的区间数

为什么这样做是对的？
- 贪心策略：总是选择结束时间最早的区间
- 这样给后续区间留的空间最大
- 可以选择最多的不重叠区间

复杂度分析
---------
时间复杂度：O(n log n)
- 排序：O(n log n)
- 遍历：O(n)
- 总时间：O(n log n)

空间复杂度：O(log n)
- 排序的递归栈空间

其中：
n = intervals.length
*/
class Solution {
public:
    int eraseOverlapIntervals(vector<vector<int>>& intervals) {
        if (intervals.size() <= 1) {
            return 0;
        }

        // 按区间结束时间排序
        sort(intervals.begin(), intervals.end(), [](const vector<int>& a, const vector<int>& b) {
            return a[1] < b[1];
        });

        // 选择第一个区间（结束时间最早）
        int count = 1;  // 选择的区间数量
        int end = intervals[0][1];  // 上一个选择区间的结束时间

        // 遍历剩余区间
        for (int i = 1; i < intervals.size(); i++) {
            // 如果当前区间的起始时间 >= 上一个选择区间的结束时间（不重叠）
            if (intervals[i][0] >= end) {
                count++;  // 选择当前区间
                end = intervals[i][1];  // 更新结束时间
            }
            // 否则，当前区间与上一个选择区间重叠，跳过（移除）
        }

        // 返回需要移除的区间数量
        return intervals.size() - count;
    }
};

/*
方法二：动态规划 (Approach 2: Dynamic Programming)

核心思想
--------
将问题转化为：求最长不重叠区间序列的长度。
使用动态规划求解最长不重叠区间序列。

算法流程：
1. 按区间起始时间排序
2. 定义 dp[i]：以第 i 个区间结尾的最长不重叠区间序列的长度
3. 状态转移：
   - 对于每个区间 i，遍历所有 j < i
   - 如果区间 j 和区间 i 不重叠（intervals[j][1] <= intervals[i][0]）
     - dp[i] = max(dp[i], dp[j] + 1)
4. 返回：总区间数 - max(dp)

复杂度分析
---------
时间复杂度：O(n²)
- 排序：O(n log n)
- 动态规划：O(n²)
- 总时间：O(n²)

空间复杂度：O(n)
- dp 数组：O(n)

对比方法一：
- 方法一（贪心）：O(n log n)，更优
- 方法二（DP）：O(n²)，较慢
- 贪心能解决的问题，不需要用 DP
*/
class Solution_DP {
public:
    int eraseOverlapIntervals(vector<vector<int>>& intervals) {
        if (intervals.size() <= 1) {
            return 0;
        }

        // 按区间起始时间排序
        sort(intervals.begin(), intervals.end(), [](const vector<int>& a, const vector<int>& b) {
            return a[0] < b[0];
        });

        int n = intervals.size();
        vector<int> dp(n, 1);  // dp[i]：以第 i 个区间结尾的最长不重叠区间序列的长度

        // 动态规划
        for (int i = 1; i < n; i++) {
            for (int j = 0; j < i; j++) {
                // 如果区间 j 和区间 i 不重叠
                if (intervals[j][1] <= intervals[i][0]) {
                    dp[i] = max(dp[i], dp[j] + 1);
                }
            }
        }

        // 找到最长不重叠区间序列的长度
        int maxLen = *max_element(dp.begin(), dp.end());

        // 返回需要移除的区间数量
        return n - maxLen;
    }
};

/*
两种方法对比 (Comparison)
------------------------
方法一：贪心（按结束时间排序）
- 时间复杂度：O(n log n)
- 空间复杂度：O(log n)
- 优点：效率高，代码简洁
- 缺点：需要理解贪心策略的正确性

方法二：动态规划
- 时间复杂度：O(n²)
- 空间复杂度：O(n)
- 优点：思路直观，易于理解
- 缺点：效率低，不适合大数据

推荐方案：
- 面试中：方法一（贪心）
- 学习理解：两种方法都要掌握

贪心策略的关键
------------
1. 为什么按结束时间排序？
   - 结束时间越早，给后续区间留的空间越大
   - 可以选择更多的区间

2. 为什么不按起始时间排序？
   - 按起始时间排序，无法做出最优选择
   - 例如：[[1,10],[2,3],[4,5]]
   - 按起始时间：选 [1,10]，只能选 1 个
   - 按结束时间：选 [2,3] 和 [4,5]，可以选 2 个

3. 贪心策略的正确性：
   - 局部最优 -> 全局最优
   - 每次选择结束时间最早的区间
   - 给后续区间留最大空间
   - 可以选择最多的区间

4. 与其他区间问题的区别：
   - 合并区间（LeetCode 56）：按起始时间排序
   - 无重叠区间（LeetCode 435）：按结束时间排序
   - 会议室（LeetCode 252/253）：按起始时间排序
*/

/*
示例推演 (Example Walkthrough)
-----------------------------
输入：intervals = [[1,2],[2,3],[3,4],[1,3]]

方法一：贪心（按结束时间排序）
---------------------------
步骤 1：按结束时间排序
intervals = [[1,2],[2,3],[1,3],[3,4]]

步骤 2：贪心选择
初始：count = 1, end = 2（选择 [1,2]）

i = 1, 区间 [2,3]：
- 2 >= 2（不重叠）
- 选择，count = 2, end = 3

i = 2, 区间 [1,3]：
- 1 < 3（重叠）
- 跳过（移除）

i = 3, 区间 [3,4]：
- 3 >= 3（不重叠）
- 选择，count = 3, end = 4

步骤 3：计算结果
移除数量 = 4 - 3 = 1

输出：1

方法二：动态规划
--------------
步骤 1：按起始时间排序
intervals = [[1,2],[1,3],[2,3],[3,4]]

步骤 2：动态规划
dp[0] = 1（[1,2]）
dp[1] = 1（[1,3]，与 [1,2] 重叠）
dp[2] = 2（[2,3]，可以接在 [1,2] 后面）
dp[3] = 3（[3,4]，可以接在 [2,3] 后面）

步骤 3：计算结果
最长不重叠区间序列长度 = 3
移除数量 = 4 - 3 = 1

输出：1

为什么 [[1,10],[2,3],[4,5]] 按结束时间排序更优？
--------------------------------------------
按起始时间排序：[[1,10],[2,3],[4,5]]
- 选择 [1,10]，end = 10
- [2,3]：2 < 10（重叠），跳过
- [4,5]：4 < 10（重叠），跳过
- 只能选 1 个区间

按结束时间排序：[[2,3],[4,5],[1,10]]
- 选择 [2,3]，end = 3
- [4,5]：4 >= 3（不重叠），选择，end = 5
- [1,10]：1 < 5（重叠），跳过
- 可以选 2 个区间

结论：按结束时间排序可以选择更多的区间
*/

/*
特殊情况 (Edge Cases)
--------------------
1. 只有一个区间：
   - 输入：[[1,2]]
   - 输出：0

2. 所有区间都不重叠：
   - 输入：[[1,2],[2,3],[3,4]]
   - 输出：0

3. 所有区间都重叠：
   - 输入：[[1,2],[1,2],[1,2]]
   - 输出：2
   - 只能保留 1 个

4. 区间边界相接：
   - 输入：[[1,2],[2,3]]
   - 输出：0
   - 2 >= 2，不算重叠

5. 一个区间包含多个区间：
   - 输入：[[1,10],[2,3],[4,5]]
   - 输出：1
   - 移除 [1,10]，保留 [2,3] 和 [4,5]

6. 多个区间有相同的结束时间：
   - 输入：[[1,5],[2,5],[3,5]]
   - 输出：2
   - 按结束时间排序后，选择第一个即可

7. 负数区间：
   - 输入：[[-5,-3],[-4,-1],[-2,0]]
   - 输出：1
   - 移除 [-4,-1]
*/

/*
区间调度问题总结
--------------
1. 问题类型：
   - 区间合并：合并所有重叠的区间
   - 区间插入：在已排序的区间列表中插入新区间
   - 区间重叠判断：判断是否存在重叠
   - 区间调度：选择最多的不重叠区间
   - 会议室分配：计算需要的最少会议室数量

2. 排序策略：
   - 按起始时间：合并区间、插入区间、会议室问题
   - 按结束时间：区间调度（贪心）
   - 按长度：特殊问题

3. 算法技巧：
   - 贪心：区间调度
   - 扫描线：会议室问题、天际线问题
   - 动态规划：最长不重叠区间序列
   - 堆：会议室分配

4. 重叠判断：
   - 排序后：当前起始 < 上一个结束
   - 未排序：max(a1, b1) < min(a2, b2)

5. 贪心正确性：
   - 局部最优 -> 全局最优
   - 需要证明贪心策略的正确性
   - 通常通过反证法或交换论证

关键区别：
- LeetCode 56（合并区间）：按起始时间排序，合并重叠区间
- LeetCode 435（无重叠区间）：按结束时间排序，贪心选择
- LeetCode 452（用最少的箭引爆气球）：与 435 类似，按结束时间排序
- LeetCode 253（会议室 II）：扫描线或堆，求最大重叠数
*/

