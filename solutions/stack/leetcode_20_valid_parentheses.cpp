/*
LeetCode 20. 有效的括号 (Valid Parentheses)

题目概述 (Problem Summary)
-------------------------
给定一个只包含 '(', ')', '{', '}', '[', ']' 的字符串 s，判断字符串是否有效。

有效字符串需满足：
1. 左括号必须用相同类型的右括号闭合
2. 左括号必须以正确的顺序闭合
3. 每个右括号都有一个对应的相同类型的左括号

关键约束 (Key Constraints)
-------------------------
- 1 <= s.length <= 10^4
- s 仅由括号 '()[]{}' 组成

示例 (Example)
-------------
输入：s = "()"
输出：true

输入：s = "()[]{}"
输出：true

输入：s = "(]"
输出：false

输入：s = "([)]"
输出：false
解释：虽然左右括号数量匹配，但顺序不对

输入：s = "{[]}"
输出：true

算法思路 (Algorithm Thinking)
----------------------------
这道题本质在解决什么问题？
- 括号匹配问题，需要检查括号的配对和顺序

关键观察：
1. 最近遇到的左括号应该最先被匹配（后进先出）
2. 这是典型的栈应用场景
3. 遇到左括号入栈，遇到右括号检查栈顶是否匹配
4. 最后栈应该为空

根据这些观察，可以得到几种典型解法：
1. 栈 + 匹配检查（最优）
2. 栈 + 哈希表映射
3. 计数法（仅适用于单一类型括号）
*/

#include <iostream>
#include <stack>
#include <string>
#include <unordered_map>
using namespace std;

/*
方法一：我的原始解法 - 栈 + 预存右括号 (Approach 1: Stack with Right Bracket)

核心思想
--------
遇到左括号时，将对应的右括号入栈。
遇到右括号时，检查栈顶是否匹配。

算法流程：
1. 遍历字符串
2. 遇到左括号 '(', '[', '{' 时，将对应的右括号 ')', ']', '}' 入栈
3. 遇到右括号时：
   - 如果栈为空，说明没有对应的左括号，返回 false
   - 如果栈顶不匹配，返回 false
   - 否则弹出栈顶
4. 最后检查栈是否为空

为什么预存右括号？
- 简化匹配逻辑，直接比较字符即可
- 避免了左右括号的映射关系

复杂度分析
---------
时间复杂度：O(n)
- 遍历字符串一次

空间复杂度：O(n)
- 最坏情况下所有字符都是左括号

其中：
n = s.length
*/
class Solution {
public:
    bool isValid(string s) {
        stack<char> st;

        for (char c : s) {
            // 遇到左括号，将对应的右括号入栈
            if (c == '(') st.push(')');
            else if (c == '[') st.push(']');
            else if (c == '{') st.push('}');
            // 遇到右括号，检查栈顶是否匹配
            else {
                // 栈为空或栈顶不匹配
                if (st.empty() || st.top() != c) {
                    return false;
                }
                st.pop();
            }
        }

        // 最后栈应该为空
        return st.empty();
    }
};

/*
方法二：栈 + 哈希表映射 (Approach 2: Stack with HashMap)

核心思想
--------
使用哈希表存储左右括号的映射关系。
遇到左括号入栈，遇到右括号检查栈顶。

算法流程：
1. 创建哈希表存储括号映射：'(' -> ')', '[' -> ']', '{' -> '}'
2. 遍历字符串
3. 遇到左括号（在哈希表的 key 中），入栈
4. 遇到右括号：
   - 如果栈为空，返回 false
   - 弹出栈顶，检查映射关系是否匹配
5. 最后检查栈是否为空

复杂度分析
---------
时间复杂度：O(n)
空间复杂度：O(n)
*/
class Solution_HashMap {
public:
    bool isValid(string s) {
        // 左右括号映射
        unordered_map<char, char> pairs = {
            {'(', ')'},
            {'[', ']'},
            {'{', '}'}
        };

        stack<char> st;

        for (char c : s) {
            // 如果是左括号，入栈
            if (pairs.count(c)) {
                st.push(c);
            }
            // 如果是右括号
            else {
                // 栈为空，没有对应的左括号
                if (st.empty()) {
                    return false;
                }

                // 检查栈顶的左括号是否匹配
                char left = st.top();
                st.pop();

                if (pairs[left] != c) {
                    return false;
                }
            }
        }

        return st.empty();
    }
};

/*
示例推演 (Example Walkthrough)
-----------------------------
输入：s = "([)]"

使用方法一：

步骤 1：c = '('
- 是左括号，入栈 ')'
- 栈：[')']

步骤 2：c = '['
- 是左括号，入栈 ']'
- 栈：[')', ']']

步骤 3：c = ')'
- 是右括号，检查栈顶
- 栈顶是 ']'，不匹配 ')'
- 返回 false

结果：false（括号顺序不对）

---

输入：s = "{[]}"

步骤 1：c = '{'
- 是左括号，入栈 '}'
- 栈：['}']

步骤 2：c = '['
- 是左括号，入栈 ']'
- 栈：['}', ']']

步骤 3：c = ']'
- 是右括号，检查栈顶
- 栈顶是 ']'，匹配
- 弹出栈顶
- 栈：['}']

步骤 4：c = '}'
- 是右括号，检查栈顶
- 栈顶是 '}'，匹配
- 弹出栈顶
- 栈：[]

步骤 5：遍历结束
- 栈为空
- 返回 true

结果：true
*/

/*
复杂度对比 (Complexity Comparison)
---------------------------------
方法一：栈 + 预存右括号
- 时间复杂度：O(n)
- 空间复杂度：O(n)
- 优点：代码简洁，逻辑清晰
- 缺点：需要记住左右括号的对应关系

方法二：栈 + 哈希表
- 时间复杂度：O(n)
- 空间复杂度：O(n)
- 优点：映射关系清晰，易于扩展
- 缺点：需要额外的哈希表空间

两种方法时间复杂度相同，方法一更简洁。
*/

/*
特殊情况 (Edge Cases)
--------------------
1. 空字符串：s = ""
   - 返回 true（没有括号，有效）

2. 单个左括号：s = "("
   - 栈不为空，返回 false

3. 单个右括号：s = ")"
   - 栈为空，返回 false

4. 只有一种括号：s = "(())"
   - 正常处理，返回 true

5. 多种括号混合：s = "([{}])"
   - 正常处理，返回 true

6. 括号数量不匹配：s = "((("
   - 栈不为空，返回 false

7. 括号顺序错误：s = "([)]"
   - 栈顶不匹配，返回 false
*/

int main() {
    Solution solution;

    // 测试用例 1：简单匹配
    string s1 = "()";
    cout << "测试用例 1: \"" << s1 << "\" -> "
         << (solution.isValid(s1) ? "true" : "false")
         << " (期望: true)" << endl;

    // 测试用例 2：多种括号
    string s2 = "()[]{}";
    cout << "测试用例 2: \"" << s2 << "\" -> "
         << (solution.isValid(s2) ? "true" : "false")
         << " (期望: true)" << endl;

    // 测试用例 3：不匹配
    string s3 = "(]";
    cout << "测试用例 3: \"" << s3 << "\" -> "
         << (solution.isValid(s3) ? "true" : "false")
         << " (期望: false)" << endl;

    // 测试用例 4：顺序错误
    string s4 = "([)]";
    cout << "测试用例 4: \"" << s4 << "\" -> "
         << (solution.isValid(s4) ? "true" : "false")
         << " (期望: false)" << endl;

    // 测试用例 5：嵌套匹配
    string s5 = "{[]}";
    cout << "测试用例 5: \"" << s5 << "\" -> "
         << (solution.isValid(s5) ? "true" : "false")
         << " (期望: true)" << endl;

    // 测试用例 6：单个左括号
    string s6 = "(";
    cout << "测试用例 6: \"" << s6 << "\" -> "
         << (solution.isValid(s6) ? "true" : "false")
         << " (期望: false)" << endl;

    // 测试用例 7：单个右括号
    string s7 = ")";
    cout << "测试用例 7: \"" << s7 << "\" -> "
         << (solution.isValid(s7) ? "true" : "false")
         << " (期望: false)" << endl;

    return 0;
}

