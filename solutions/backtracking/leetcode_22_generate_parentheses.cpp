/*
LeetCode 22. 括号生成 (Generate Parentheses)

题目概述 (Problem Summary)
-------------------------
数字 n 代表生成括号的对数，请你设计一个函数，用于能够生成所有可能的并且有效的括号组合。

关键约束 (Key Constraints)
-------------------------
- 1 <= n <= 8
- 括号必须正确闭合
- 左括号必须在对应的右括号之前

示例 (Example)
-------------
输入：n = 3
输出：["((()))","(()())","(())()","()(())","()()()"]

输入：n = 1
输出：["()"]

输入：n = 2
输出：["(())","()()"]

算法思路 (Algorithm Thinking)
----------------------------
这道题本质在解决什么问题？
- 生成所有有效的括号组合，需要满足括号匹配规则

关键观察：
1. 这是一个典型的回溯问题
2. 有效括号的特征：
   - 左括号数量 = 右括号数量 = n
   - 任意前缀中，左括号数量 >= 右括号数量
3. 可以通过剪枝避免生成无效组合
4. 使用两个计数器追踪左右括号的使用情况

回溯决策树：
以 n = 3 为例：

                        ""
                        |
                       (
                    /     \
                  ((        ()
                /   \      /  \
              (((   (()   ()(  ()()
             /     / | \    |    |
          ((()))  (()()) ...  ...

每个节点有两个选择：
1. 添加左括号（如果 left < n）
2. 添加右括号（如果 right < left）

根据这些观察，可以得到几种典型解法：
1. 回溯法 + 剪枝 - 标准解法
2. 动态规划 - 利用子问题的解
3. 闭合数法 - 数学方法
*/

#include <iostream>
#include <vector>
#include <string>

using namespace std;

/*
方法一：我的原始解法 - 回溯法 + 剪枝 (Approach 1: My Original Solution - Backtracking with Pruning)

核心思想
--------
使用回溯法生成所有可能的括号组合，通过剪枝保证生成的都是有效组合。
维护两个计数器：left（已使用的左括号数）和 right（已使用的右括号数）。

算法流程：
1. 从空字符串开始回溯
2. 在每个位置，尝试两种选择：
   a. 添加左括号（条件：left < n）
   b. 添加右括号（条件：right < left）
3. 当字符串长度达到 2*n 时，加入结果集
4. 回溯：撤销刚才的选择

为什么使用回溯？
- 需要生成所有可能的有效组合
- 可以通过剪枝提前排除无效组合
- 自然地表达"选择-递归-撤销"的过程

剪枝条件：
1. left < n：还可以添加左括号
2. right < left：右括号数量不能超过左括号（保证有效性）

复杂度分析
---------
时间复杂度：O(4^n / √n)
- 这是第 n 个卡特兰数的渐进上界
- 实际生成的有效括号组合数为卡特兰数 C_n = (2n)! / ((n+1)! * n!)
- 每个组合需要 O(n) 时间构建

空间复杂度：O(n)
- 递归调用栈的深度为 2n
- 不计算结果数组的空间

其中：
n = 括号对数
*/
class Solution {
public:
    vector<string> ans;  // 存储所有有效的括号组合
    string path;         // 当前路径（正在构建的括号序列）

    // 回溯函数
    // n: 括号对数
    // left: 已使用的左括号数量
    // right: 已使用的右括号数量
    void backtrack(int n, int left, int right){
        // 递归终止条件：已经生成了 2n 个括号
        if(path.size() == 2 * n){
            ans.push_back(path);  // 将当前有效组合加入结果
            return;
        }

        // 选择1：添加左括号
        // 剪枝条件：左括号数量不能超过 n
        if(left < n){
            path.push_back('(');           // 做选择
            backtrack(n, left + 1, right); // 递归
            path.pop_back();               // 撤销选择（回溯）
        }

        // 选择2：添加右括号
        // 剪枝条件：右括号数量不能超过左括号（保证括号匹配）
        if(right < left){
            path.push_back(')');           // 做选择
            backtrack(n, left, right + 1); // 递归
            path.pop_back();               // 撤销选择（回溯）
        }
    }

    vector<string> generateParenthesis(int n){
        // 从 0 个左括号和 0 个右括号开始回溯
        backtrack(n, 0, 0);
        return ans;
    }
};

/*
方法二：动态规划 (Approach 2: Dynamic Programming)

核心思想
--------
利用子问题的解来构建当前问题的解。
dp[i] 表示 i 对括号的所有有效组合。

递推关系：
对于 n 对括号，可以将其分解为：
"(" + p对括号 + ")" + q对括号
其中 p + q = n - 1

dp[n] = "(" + dp[p] + ")" + dp[q]，其中 p + q = n - 1

算法流程：
1. 初始化：dp[0] = {""}
2. 对于每个 i (1 到 n)：
   - 枚举 p (0 到 i-1)，q = i - 1 - p
   - 组合 dp[p] 和 dp[q] 的所有可能
3. 返回 dp[n]

复杂度分析
---------
时间复杂度：O(4^n / √n)
- 与回溯法相同，因为需要生成所有有效组合

空间复杂度：O(4^n / √n)
- 需要存储所有中间结果

其中：
n = 括号对数
*/
class Solution_DP {
public:
    vector<string> generateParenthesis(int n) {
        // dp[i] 存储 i 对括号的所有有效组合
        vector<vector<string>> dp(n + 1);
        dp[0] = {""};  // 0 对括号只有空字符串

        // 逐步构建 1 到 n 对括号的解
        for(int i = 1; i <= n; i++){
            // 枚举内部括号对数 p，外部括号对数 q = i - 1 - p
            for(int p = 0; p < i; p++){
                int q = i - 1 - p;

                // 组合 dp[p] 和 dp[q] 的所有可能
                for(const string& left : dp[p]){
                    for(const string& right : dp[q]){
                        // 形式：( + left + ) + right
                        dp[i].push_back("(" + left + ")" + right);
                    }
                }
            }
        }

        return dp[n];
    }
};

